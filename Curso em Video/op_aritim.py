# Operações aritiméticas

# +  Adição
# -  Subtração
# *  Multiplicação
# /  Divisão
# // Divisão de Inteira
# %  Resto de Divisão - MOD
# ** Expôente

print('Adição: ', 5 + 5)
print('Subtração: ', 10 - 5)
print('Multiplicação: ', 10 * 10)
print('Divisão: ', 10 / 2)
print('Divisão de inteiros: ', 10 // 2)
print('Resto de divisão: ', 10 % 3)
print('Expoênte: ', 10 ** 3)

#Ordem de resolução

# 1º () - Parenteses
# 2º ** - Potencias
# 3º *, /, // % - Mult, Div, Div Int, MOD
# 4º +, - -- Adição e Subtração